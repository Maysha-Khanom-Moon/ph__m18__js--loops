/**
 * for(initial, update, checking) {
 *      .. statement ..
 * }
 */

for(let num = 0; num < 5; num++) {
    console.log(num);
}

// odd
console.log('\nOdd:');
for(let i = 1; i < 10; i += 2) {
    console.log(i);
}

// even 
console.log('\nEven:');
for(let i = 2; i <= 10; i+=2) {
    console.log(i);
}